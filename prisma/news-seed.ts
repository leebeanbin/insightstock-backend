async function main() {
  console.log('🌱 Seeding news data...');

  // 기존 뉴스 데이터 삭제
  await prisma.newsKeyPoint.deleteMany({});
  await prisma.newsConcept.deleteMany({});
  await prisma.newsStock.deleteMany({});
  await prisma.news.deleteMany({});

  // 주식 코드로 ID 매핑 생성
  const stocks = await prisma.stock.findMany();
  const stockCodeToId = new Map(stocks.map((s) => [s.code, s.id]));

  let newsCount = 0;

  // 카테고리별 뉴스 생성 (positive)
  for (const [category, newsList] of Object.entries(NEWS_BY_CATEGORY)) {
    for (const newsData of newsList) {
      // 뉴스 생성
      const news = await prisma.news.create({
        data: {
          title: newsData.title,
          content: newsData.content,
          summary: newsData.summary,
          source: newsData.source,
          url: newsData.url,
          publishedAt: newsData.publishedAt,
          sentiment: newsData.sentiment,
          sentimentScore: newsData.sentimentScore,
          thumbnailUrl: newsData.thumbnailUrl,
        },
      });

      // 뉴스-주식 연결
      for (const stockCode of newsData.stockCodes) {
        const stockId = stockCodeToId.get(stockCode);
        if (stockId) {
          await prisma.newsStock.create({
            data: {
              newsId: news.id,
              stockId: stockId,
            },
          });
        }
      }

      // 핵심 포인트 생성
      for (const keyPoint of newsData.keyPoints) {
        await prisma.newsKeyPoint.create({
          data: {
            newsId: news.id,
            content: keyPoint,
          },
        });
      }

      // 개념 생성
      for (const concept of newsData.concepts) {
        await prisma.newsConcept.create({
          data: {
            newsId: news.id,
            concept: concept,
          },
        });
      }

      newsCount++;
    }
  }

  // Negative 뉴스 생성
  for (const [category, newsList] of Object.entries(NEGATIVE_NEWS)) {
    for (const newsData of newsList) {
      const news = await prisma.news.create({
        data: {
          title: newsData.title,
          content: newsData.content,
          summary: newsData.summary,
          source: newsData.source,
          url: newsData.url,
          publishedAt: newsData.publishedAt,
          sentiment: newsData.sentiment,
          sentimentScore: newsData.sentimentScore,
          thumbnailUrl: newsData.thumbnailUrl,
        },
      });

      for (const stockCode of newsData.stockCodes) {
        const stockId = stockCodeToId.get(stockCode);
        if (stockId) {
          await prisma.newsStock.create({
            data: {
              newsId: news.id,
              stockId: stockId,
            },
          });
        }
      }

      for (const keyPoint of newsData.keyPoints) {
        await prisma.newsKeyPoint.create({
          data: {
            newsId: news.id,
            content: keyPoint,
          },
        });
      }

      for (const concept of newsData.concepts) {
        await prisma.newsConcept.create({
          data: {
            newsId: news.id,
            concept: concept,
          },
        });
      }

      newsCount++;
    }
  }

  // Neutral 뉴스 생성
  for (const [category, newsList] of Object.entries(NEUTRAL_NEWS)) {
    for (const newsData of newsList) {
      const news = await prisma.news.create({
        data: {
          title: newsData.title,
          content: newsData.content,
          summary: newsData.summary,
          source: newsData.source,
          url: newsData.url,
          publishedAt: newsData.publishedAt,
          sentiment: newsData.sentiment,
          sentimentScore: newsData.sentimentScore,
          thumbnailUrl: newsData.thumbnailUrl,
        },
      });

      for (const stockCode of newsData.stockCodes) {
        const stockId = stockCodeToId.get(stockCode);
        if (stockId) {
          await prisma.newsStock.create({
            data: {
              newsId: news.id,
              stockId: stockId,
            },
          });
        }
      }

      for (const keyPoint of newsData.keyPoints) {
        await prisma.newsKeyPoint.create({
          data: {
            newsId: news.id,
            content: keyPoint,
          },
        });
      }

      for (const concept of newsData.concepts) {
        await prisma.newsConcept.create({
          data: {
            newsId: news.id,
            concept: concept,
          },
        });
      }

      newsCount++;
    }
  }

  console.log(`✅ Seeded ${newsCount} news articles (positive, negative, neutral) across ${Object.keys(NEWS_BY_CATEGORY).length} categories`);
}

